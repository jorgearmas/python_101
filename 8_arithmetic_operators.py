"""
Operadores aritméticos
"""
#suma
print(f"Suma de (10+10): {10+10}")
#resta
print(f"Resta de (10-5): {10-5}")
#multiplicación
print(f"Multiplicación de (10*2): {10*2}")
#división
print(f"División de (10/2): {10/2}")
#división - cociente entero
print(f"División exacta de (10/2): {10//2}")
#módulo
print(f"Módulo de (10%2): {10%2}")
#potencia
print(f"Potencia de (2**3): {2**3}")

#suma de cadenas
print(f"Suma de 'hola' + 'mundo': {'hola '+'mundo'}")
#multiplicación de cadenas
print(f"Multiplicación de 'hola' tres veces: {'hola'*3}")